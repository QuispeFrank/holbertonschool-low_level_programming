#include "3-calc.h"

/**
 * op_add - add
 * @a: integer
 * @b: integer
 */

int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - subtract
 * @a: integer
 * @b: integer
 */
int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - multiply
 * @a: integer
 * @b: integer
 */

int op_mul(int a, int b)
{	
	return (a * b);
}


/**
 * op_div - divide
 * @a: integer
 * @b: integer
 */

int op_div(int a, int b)
{	
	return (a / b);
}


/**
 * op_mod - module
 * @a: integer
 * @b: integer
 */

int op_mod(int a, int b)
{	
	return (a % b);
}
