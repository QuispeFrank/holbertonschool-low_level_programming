#include "3-calc.h"

/**
 * op_add - op_add nu
 * @a: integer
 * @b: integer
 * Return: int
 */

int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - subtract nub
 * @a: integer
 * @b: integer
 * Return: int
 */
int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - multiply nub
 * @a: integer
 * @b: integer
 * Return: int
 */

int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - divide nub
 * @a: integer
 * @b: integer
 * Return: int
 */

int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - module nub
 * @a: integer
 * @b: integer
 * Return: int
 */

int op_mod(int a, int b)
{
	return (a % b);
}
